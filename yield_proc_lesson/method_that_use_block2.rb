# yield（ブロック）に引数を渡し、ブロックの戻り値を受け取ってputsメソッドで出力するコード例
def greeting
  puts 'おはよう'
  # ブロックに引数を渡し、戻り値を受け取る
  text = yield('こんにちは')
  puts text
  puts 'こんばんわ'
end

greeting do |text|
  #yieldで渡された文字列("こんにちは")を2回繰り返す
  text * 2
end

#=> おはよう
# こんにちはこんにちは
# こんばんわ

