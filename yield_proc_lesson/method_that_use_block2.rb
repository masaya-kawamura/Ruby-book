# yieldで渡した引数がブロック数より多かったり、ブロック引数がyieldで渡した引数より多かったりしてもエラーにならない

def greeting
  puts 'おはよう'
  # 2個の引数をブロックに渡す
  text = yield('こんにちは', 12345)
  puts text
  puts 'こんばんわ'
end

greeting do |text|
  #ブロック引数が1つであればm2つ目の引数は無視される
  text * 2
end

#=> おはよう
# こんにちはこんにちは
# こんばんわ

