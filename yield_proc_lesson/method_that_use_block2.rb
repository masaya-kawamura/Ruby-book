# ブロックの引数を2つ設定すると...

def greeting
  puts 'おはよう'
  # 1この引数をブロックに渡す
  text = yield 'こんにちは'
  puts text
  puts 'こんばんわ'
end

greeting do |text, other|
  #ブロック引数が2つあれば、2つ目の引数はnilになる
  text * 2 + other.inspect
end


#=> おはよう
# こんにちはこんにちはnil
# こんばんわ

